
public class Skills {

	private String sname; 
	private int pid;
	
	
	public Skills(String sname, int pid){
		this.sname = sname;
		this.pid =pid; 
	}
	
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	} 
	
	
}
