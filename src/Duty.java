
public class Duty {

	private String duty; 
	private int expid;
	
	Duty(String duty, int expid){ 
		this.duty = duty; 
		this.expid = expid; 
		
	}
	
	public String getDuty() {
		return duty;
	}
	public void setDuty(String duty) {
		this.duty = duty;
	}
	public int getExpid() {
		return expid;
	}
	public void setExpid(int expid) {
		this.expid = expid;
	} 
	
	
	
	
	
	
}
